    @Override
    public void windowOpened(WindowEvent e) {
        ResultSet rs = null;
        try {
            DBConnect db = new DBConnect();
            rs = db.getReminder(UserDatabase.key);
            
            if (rs.next()) {
                String remind = rs.getString("reminder");
                jTextArea1.setText(remind);
            }
        }
        catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    @Override
    public void windowClosing(WindowEvent e) {
        String reminderText = jTextArea1.getText();

        try {
            DBConnect db = new DBConnect();
            db.updateReminder(UserDatabase.key, reminderText);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

