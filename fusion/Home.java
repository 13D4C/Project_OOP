    public void showLineChart() {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        
        ResultSet rs = null;
        double[] months = new double[12];
        String[] monthNames = {"jan", "feb", "mar", "apr", "may", "june", "july", "aug", "sept", "octo", "nov", "dece"};
        String[] monthNames2 = {"Jan", "Feb", "Mar", "Apr", "May", "June", "July", "Aug", "Sep", "Oct", "Nov", "Dec"};

        
        try {
            DBConnect db = new DBConnect();
            rs = db.getMonthSale(UserDatabase.key);
            
            while (rs.next()) {
                for (int i = 0; i < monthNames.length; i++) {
                    months[i] += Double.parseDouble(rs.getString(monthNames[i]));
                }
            }
        }
        catch (Exception ex) {
            ex.printStackTrace();
        }

        for (int i = 0; i < monthNames.length; i++) {
            dataset.setValue(months[i], "amount", monthNames2[i]);
        }
        
        JFreeChart Chart = ChartFactory.createBarChart("Monthly Sales", "Monthly", "Amount", dataset, PlotOrientation.VERTICAL.VERTICAL, false, true, false);
    
        CategoryPlot Category = Chart.getCategoryPlot();
        Category.setBackgroundPaint(Color.white);
        
        BarRenderer Renderer = (BarRenderer) Category.getRenderer();
        Color ChartColor = new Color(74, 134, 228);
        Renderer.setSeriesPaint(0, ChartColor);
        
        ChartPanel ChartPanel = new ChartPanel(Chart);
        EmptyPanelTopRight.removeAll();
        EmptyPanelTopRight.add(ChartPanel, BorderLayout.CENTER);
        EmptyPanelTopRight.validate();
    }
 
    public void showPieChart() {
        
        DefaultPieDataset dataset = new DefaultPieDataset();

        ResultSet rs = null;
        ArrayList<String> item = new ArrayList<String>();

        String[] hexColors = {"#4A86E4", "#222683", "#D377F3", "#1D1F24", "#3A3D44", "#429482", "#459CED", "#5D923D", "#6B6E75", "#7662EA", "#E55D57", "#EF746D"};
        Color[] colors = new Color[hexColors.length];

        for (int i = 0; i < hexColors.length; i++) {
            try {
                colors[i] = Color.decode(hexColors[i]);
            } catch (NumberFormatException e) {
                e.printStackTrace();
                colors[i] = Color.BLACK;
            }
        }
        
        try {
            DBConnect db = new DBConnect();
            rs = db.getNameItem(UserDatabase.key);
            
            while (rs.next()) {
                dataset.setValue(rs.getString("item"), Integer.parseInt(rs.getString("sales")));
                item.add(rs.getString("item"));
            }
        }
        catch (Exception ex) {
            ex.printStackTrace();
        }
        
        
        JFreeChart pieChart = ChartFactory.createPieChart("Top Sales", dataset, false, true, false);
    
        PiePlot piePlot = (PiePlot) pieChart.getPlot();
        piePlot.setBackgroundPaint(Color.white);
        
        for (int i = 0; i < item.size(); i++) {
            piePlot.setSectionPaint(i, colors[i % colors.length]);
        }
        
        ChartPanel ChartPanel = new ChartPanel(pieChart);
        EmptyPanelTopLeft.removeAll();
        EmptyPanelTopLeft.add(ChartPanel, BorderLayout.CENTER);
        EmptyPanelTopLeft.validate();
    }
    
